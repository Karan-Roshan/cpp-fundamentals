// print the area and circumference of the circle
#include <iostream>
using namespace std;

int main()
{
    int r;
    cout << "Enter the radius(r) of the circle: ";
    cin >> r;

    float area = 3.14 * r * r;
    float circumference = 2 * 3.4 * r;
    cout << "Area of the circle: " << area << endl;
    cout << "Circumference of the circle: " << circumference;
    return 0;
}